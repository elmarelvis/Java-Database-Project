    public ArrayList<Student> getStudentsList() {
        ArrayList<Student> studentsList = new ArrayList<Student>();
        Connection connection = getConnection();
        String query = "SELECT * FROM students ";
        Statement st;
        ResultSet rs;

        try {
            st = connection.createStatement();
            rs = st.executeQuery(query);
            Student student;
            while (rs.next()) {
                student = new Student(rs.getInt("ID"), rs.getString("Name"), rs.getInt("Tel."), rs.getInt("Class"), rs.getInt("Age"));
                studentsList.add(student);
            }

        } catch (SQLException ex) {
        }
        return studentsList;
    }

    public void Show_Students_In_Jtable() {
        ArrayList<Student> list = getStudentsList();
        DefaultTableModel model = (DefaultTableModel) jTable_Display_Students.getModel();
        Object[] row = new Object[5];
        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getId();
            row[1] = list.get(i).getnAme();
            row[2] = list.get(i).getTelephone();
            row[3] = list.get(i).getClasss();
            row[4] = list.get(i).getAge();
            model.addRow(row);
        }
    }

    public void executeSQLQuery(String query, String message) throws SQLException {
        Connection con = getConnection();
        Statement st;
        try {
            st = con.createStatement();
            if ((st.executeUpdate(query)) == 1) {
                
                // refresh data in database
                DefaultTableModel model = (DefaultTableModel) jTable_Display_Students.getModel();
                model.setRowCount(0);
                Show_Students_In_Jtable();
                
                JOptionPane.showMessageDialog(null, "Data" + message + "Succesfully");
            } else {
                JOptionPane.showMessageDialog(null, "Data not" + message);
            }
        } catch (Exception ex) {
            ex.printStackTrace();

        }

    }

