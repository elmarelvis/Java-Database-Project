    public Connection getConnection() {
        Connection con;
        try {
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/students?zeroDateTimeBehavior=convertToNull", "root", "");
            return con;
        } catch (SQLException e) {
            return null;
        }
    }

